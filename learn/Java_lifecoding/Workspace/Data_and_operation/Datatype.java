public class Datatype {
	public static void main(String[] args) {
		System.out.println(6); // Number 6 출력
		// sysout 스니펫 작성 후 Ctrl + Space 누르면 해당 구문 자동 생성
		System.out.println("six"); // String "six" 출력
		
		System.out.println(6 + 6); // Number 6+6 출력
		System.out.println("six" + "six"); // String "six" 와 "six" 더한 값 출력.
		
		System.out.println(6 * 6); // Number 6 * 6 결과 출력
		//System.out.println("6" * "6"); // String 간 곱셈 연산은 불가!
		
		System.out.println("1111".length()); // 문자열의 길이를 출력헤주는 메소드.
		//System.out.println(1111.length()); // Number는 배열이 아니므로 길이를 못잼.
		
	}
}